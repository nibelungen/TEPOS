	private void toggleReceiptPrinterOsView(boolean view) {
		cbReceiptPrinterName.setVisible(view);
	}

	private void toggleReceiptPrinterJavaPosView(boolean view) {
		lblReceiptPrinterName.setVisible(view);
		lblReceiptCashDrawerName.setVisible(view);
		tfReceiptPrinterName.setVisible(view);
		tfReceiptCashDrawerName.setVisible(view);
	}

